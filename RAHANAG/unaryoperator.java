class unaryoperator
{
    public static void main(String args[])
    {  
        int a=20;  
        int b=-20;  
        boolean c=false;  
        boolean d=true;  
        System.out.println(~a);
        System.out.println(~b); 
        System.out.println(!c); 
        System.out.println(!d);
    }  
}
