class elseifdemo
{ 
    public static void main(String args[]) 
    { 
        int i = 40; 
  
        if (i == 25) 
            System.out.println("i is 25"); 
        else if (i == 30) 
            System.out.println("i is 30"); 
        else if (i == 35) 
            System.out.println("i is 35"); 
        else
            System.out.println("i is not present"); 
    } 
} 